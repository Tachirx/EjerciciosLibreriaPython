"""
Módulo de operaciones matemáticas básicas.
Este módulo contiene funciones para sumar, restar,
multiplicar y dividir.
"""

PI = 3.14159

def sumar(a, b):
    """Retorna la suma de dos números."""
    return a + b

def restar(a, b):
    """Retorna la resta de dos números."""
    return a - b

def multiplicar(a, b):
    """Retorna la multiplicación de dos números."""
    return a * b